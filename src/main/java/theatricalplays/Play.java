// Package pour les représentations théâtrales
package theatricalplays;

// Classe représentant une pièce de théâtre
public class Play {

  // Nom de la pièce de théâtre
  public String name;
  
  // Type de la pièce de théâtre
  public String type;

  // Constructeur de la classe Play
  public Play(String name, String type) {
    this.name = name;
    this.type = type;
  }
}
